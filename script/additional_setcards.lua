function Card.IsSetCard(c,...)
	local set={...}
	for i=1,#set do
		if Auxiliary.AdditionalSetcardsList[set[i]]
			and c:IsCode(table.unpack(Auxiliary.AdditionalSetcardsList[set[i]])) then
